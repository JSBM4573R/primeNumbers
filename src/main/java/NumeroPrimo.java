public class NumeroPrimo {

    int numero;

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    @java.lang.Override
    public java.lang.String toString() {
        return "NumeroPrimo{" +
                "numero=" + numero +
                '}';
    }
}